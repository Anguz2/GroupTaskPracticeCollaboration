public class EvenOrOdd {
    public static void main(String[] args) {

        evenOrOdd(10);

    }
    public static void evenOrOdd (int num){
        if (num % 2 == 0){
            System.out.println("The number is Even");
        }
        else System.out.println("The number is Odd");

        }
    }

